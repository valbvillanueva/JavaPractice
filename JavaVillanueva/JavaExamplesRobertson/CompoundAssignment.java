package JavaExamplesRobertson;

public class CompoundAssignment {
    public static void main(String[] args) {
      int value = 1;
      value += 1; // 1 + 1 = 2
      
      int value1 = 1;
      value1 -= 1;  // 1 - 1 = 0
      System.out.println(value);
      System.out.println(value1);
    }
}
