package PreBuiltMethods;

public class ConversionCharacters {
    public static void main(String[] args) {
        System.out.printf("Planet: %s\n", "Neptune"); //s	This displays a string.

        System.out.printf("Number of moons: %d\n", 13); //d	This displays a decimal integer (an int or a long).

        System.out.printf("Orbital period (in earth years): %f\n", 164.79); //f	This displays a floating-point number (a float 
        System.out.printf(
                "Average distance from the sun (in km): %e\n", 4_498_252_900.0); //e	This displays a floating-point number (float or double) 	in scientific notation.


    }

}
